package com.adfonic.adserver.simulation.model;

public class ComponentContentSpecMapModel {

	private Long componentId;
	private Long displayTypeId;
	private Long contentSpecId;
	
	public Long getComponentId() {
		return componentId;
	}
	public void setComponentId(Long componentId) {
		this.componentId = componentId;
	}
	public Long getDisplayTypeId() {
		return displayTypeId;
	}
	public void setDisplayTypeId(Long displayTypeId) {
		this.displayTypeId = displayTypeId;
	}
	public Long getContentSpecId() {
		return contentSpecId;
	}
	public void setContentSpecId(Long contentSpecId) {
		this.contentSpecId = contentSpecId;
	}
}
