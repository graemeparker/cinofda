package com.adfonic.adserver.rtb.open.v1;

public class User {
    
    private String uid;
    
    private Integer yob;
    
    private String gender;
    
    private String zip;
    
    private String country;
    
    private String nex_state;
    
    private String nex_dma;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public Integer getYob() {
        return yob;
    }

    public void setYob(Integer yob) {
        this.yob = yob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getNex_state() {
        return nex_state;
    }

    public void setNex_state(String nex_state) {
        this.nex_state = nex_state;
    }

    public String getNex_dma() {
        return nex_dma;
    }

    public void setNex_dma(String nex_dma) {
        this.nex_dma = nex_dma;
    }
    

}
