package com.adfonic.tasks.combined.truste;

public class TrusteUnreachableException extends Exception {

	public TrusteUnreachableException() {
		super();
	}

	public TrusteUnreachableException(String message, Throwable cause) {
		super(message, cause);
	}

	public TrusteUnreachableException(String message) {
		super(message);
	}

	public TrusteUnreachableException(Throwable cause) {
		super(cause);
	}

}
